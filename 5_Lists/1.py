Data = ["Name" , 32.55 , "Roll no" , 55 ]
print(Data)
# Replacing 
Data[0] = "Nitin" # it will replace name with Nitin , 0 means at index number 0
print(Data)
print(Data[3]) # printing index number 3 if we want to print at index number 4 it will get an error saying out of range
